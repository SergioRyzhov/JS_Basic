function summ(num1, num2) {
    return num1 + num2
}

function sub(num1, num2) {
    return num1 - num2
}

function dev(num1, num2) {
    return num1 / num2
}

function mul(num1, num2) {
    return num1 * num2
}

console.log(summ(1, 2))
console.log(sub(4, 1))
console.log(dev(6, 2))
console.log(mul(3, 1))